public class ExampleMonths {
    public static void main(String[] args) {
        String month = "January";
        switch (month){
            case "January":
                System.out.println("Jan");
                break;
            case "June":
                System.out.println("Jun");
                break;
            default:
                System.out.println("Not sure");
        }
    }
}
